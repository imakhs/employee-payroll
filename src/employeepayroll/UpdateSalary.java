    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(r_ind.isSelected()==true){
        try{
            salary = Integer.parseInt(txt_sal.getText());
            String getvalue1 = txt_value.getText();
                    if(!"".equals(getvalue1)){
                        getvalue = Integer.parseInt(txt_value.getText());
                    }else{
                        getvalue = 0;
                    }
            if (r_percentage.isSelected()==true){
                calculateonpercentage(); 
            } else if (r_amount.isSelected()==true){
               calculateonamount();
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        try{
            String id = txt_empid.getText();
            String sal = txt_sal.getText();
            
            String sql = "UPDATE emp_info SET id = '"+id+"', salary = '"+sal+"' WHERE id = '"+id+"'";
            pst=conn.prepareStatement(sql);
            pst.execute();
            //JOptionPane.showMessageDialog(null, "Success!");
            
        } catch(Exception e){
            
        }
        finally{
            try{
                rst.close();
                pst.close();
            } catch(Exception e){
                
            }
        }
        } else if(r_all.isSelected()==true){
            String getvalue1 = txt_value.getText();
            if(!"".equals(getvalue1)){
                        getvalue = Integer.parseInt(txt_value.getText());
                    }else{
                        getvalue = 0;
                    }
            String sql = "UPDATE emp_info SET salary = salary + " + getvalue;
            try {
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Success!");
            } catch (SQLException ex) {
                Logger.getLogger(UpdateSalary.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

