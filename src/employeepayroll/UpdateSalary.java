    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        try{
            clearvalues();
            String sql = "SELECT * FROM emp_info WHERE id = ?";
            
            pst= conn.prepareStatement(sql);
            pst.setString(1,txt_search.getText());
            rst = pst.executeQuery();
            
            String add1 = rst.getString("id");
            txt_empid.setText(add1);
            
            String add2 = rst.getString("fname");
            txt_fname.setText(add2);
            
            String add3 = rst.getString("sname");
            txt_sname.setText(add3);
            
            String add4 = rst.getString("dob");
            txt_dob.setText(add4);
            
            String add5 = rst.getString("dept");
            txt_dept.setText(add5);
            
            String add6 = rst.getString("salary");
            txt_sal.setText(add6);
            
            
            
        } catch(Exception e){
           //JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txt_searchKeyReleased

